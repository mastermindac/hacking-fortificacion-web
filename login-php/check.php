<?php
/*
CURSO: HACKING Y CERTIFICACIÓN WEB
ARCHIVO: CHECK.PHP
En este archivo php se realiza la comprobación del usuario y contraseña
que nos ha enviado el formulario de login.php
Si da error, muestra un mensaje
Si es correcto, redirige a home.php
*/
include "config.php";

if(isset($_POST['but_submit'])){

    $uname = mysqli_real_escape_string($con,$_POST['txt_uname']);
    $password = mysqli_real_escape_string($con,$_POST['txt_pwd']);

    if ($uname != "" && $password != ""){

        $sql_query = "select count(*) as cntUser from users where username='".$uname."' and password='".$password."'";
        $result = mysqli_query($con,$sql_query);
        $row = mysqli_fetch_array($result);

        $count = $row['cntUser'];

        if($count > 0){
            $_SESSION['uname'] = $uname;
            header('Location: home.php');
        }else{
            echo "Invalid username and password";
        }

    }

}